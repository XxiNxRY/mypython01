# Operator ตัวดำเนินการ คือเครื่องหมายที่มช้ในการประมวลผลข้อมูล
# 1. Arithmetic Operator เครื่องหมายคำนวณ 
# (+,-,*,/,% mod,// floor,** eponential)
print(10 + 3)
print(10 - 3)
print(10 * 3)
print(10 / 3)
print(10 % 3) # เอาแต่เศษ
print(10 // 3) # เอาแต่ส่วน
print(10 ** 3) # ยกกำลัง
print('++++++++++++++++++++++++++')
# 2. Comparison Operator เครื่องหมายเปรียบเทียบ (==, !=, >, <, >=, <=)
# ผลลัพธ์ที่ได้จะมีแค่ True/False
print(10 == 3)
print(10 != 3)
print('Bangna' >= 'Bangpoo')
print('Thailand' < 'THAILAND')
print('+++++++++++++++++++++++++++++++')
# 3. logical operator เครื่องหมาย ตรรกะ not, and, or
# ข้อมูลที่จะใช้กับเครื่องหมายนี้จะต้องเป็น True/False และผลลัพธ์ที่จะได้จากการใช้เครื่องหมาย True/False
print(not True)
print(not False)
print('------')
print(True and True) #True
print(True and False)
print(False and True)
print(False and False)
print('------')
print(True or True)
print(True or False)
print(False or True)
print(False or False) #False 