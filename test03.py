# คำสั่งแสดงผลข้อมูลใดๆ ทางหน้าจอ ใช้ฟังก์ชัน print()
# string
print('Hello')
print("Hi")
print('''Hey''')
print("""Hum""")

# number
print(1234)         # integer/int
print(3.14124)      # float

# boolean ตรรกะ มีแค่ True(จริง) และ False(เท็จ)
print(True)
print(False)

# Expression นิพจน์ คือ อะไรก็ตามที่มี process การทำงาน เช่น การคำนวณ, ตัวแปร, ...
print(10 + 20 + 5)
print(10 * 2 / 2)