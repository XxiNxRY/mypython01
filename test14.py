# if-elif-else

score = 68

if score >= 80 :
    print('Grade A')
elif score >= 70 :
    print('Grade B')
elif score >= 60 :
    print('Grade C')
elif score >= 50 :
    print('Grade D')
else : #colon อย่าลืม *** else สุดท้ายจะเป็น elif ก็ได้
    print('Grade F')

